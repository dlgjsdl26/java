package e_oop;

public class Calculator {

	// +
	double sum(double a, double b){
		return a + b;
	}
	// -
	double min(double a, double b){
		return a - b;
	}
	// /
	double nun(double a, double b){
		return a / b;
	}
	// *
	double gob(double a, double b){
		return a * b;
	}
	// %
	double nam(double a, double b){
		return a % b;
	}
}
